/**
 * 카테고리에서 랜덤 인물 반환 (사용된 인물 제외)
 */
export function getRandomPerson(category, usedNames = []) {
  const pool = TURNEYIA_DATA[category].filter((p) => !usedNames.includes(p.name));
  const source = pool.length > 0 ? pool : TURNEYIA_DATA[category];
  return source[Math.floor(Math.random() * source.length)];
}

export function getCategoryLabel(category) {
  return { celebrity: '연예인', athlete: '운동선수', politician: '정치인' }[category] || category;
}
